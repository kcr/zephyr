static const char rcsid_ZClosePort_c[] = "$Id: ZClosePort.c,v 1.8 1999-01-22 23:19:03 ghudson Exp $";
#endif

Code_t ZClosePort()
{
    if (__Zephyr_fd >= 0 && __Zephyr_open)
	(void) close(__Zephyr_fd);

    __Zephyr_fd = -1;
    __Zephyr_open = 0;
	
    return (ZERR_NONE);
}
